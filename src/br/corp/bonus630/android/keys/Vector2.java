package br.corp.bonus630.android.keys;

public class Vector2 {
	private int X;
	private int Y;
	public Vector2(int x, int y) {
		super();
		X = x;
		Y = y;
	}
	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}

}
